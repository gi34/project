    private void calcBillBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calcBillBtnActionPerformed
        // TODO add your handling code here:
        try{
            String matricTF=MatricNoTF.getText();
            if(matricTF.equals(""))
                throw new Exception("Please enter matric number");
            int matric=Integer.parseInt(matricTF);

            String name=NameTF.getText();
            if(name.equals("")){
                throw new Exception("Please enter name.");
            }
            
            String uni=UniCB.getSelectedItem().toString();
            if(DegreeRB.isSelected()==false && MasterRB.isSelected()==false)
                throw new Exception("Please select the program type");

            String program="";
            if(uni.equals("degree"))
                program="degree";
            else
                program="master";

            String weightTF= WeightTF.getText();
            if(weightTF.equals(""))
                throw new Exception ("Please enter the weight");
            Double weight=Double.parseDouble(weightTF);

            String heightTF= HeightTF.getText();
            if(heightTF.equals(""))
                throw new Exception ("Please enter the height");
            Double height=Double.parseDouble(heightTF);


            double bmi=operation.calculateBMI(height, weight);
            String status=operation.getStatusBMI(bmi);

            String BMI=String.format("%.2f", bmi);
            BMITF.setText(BMI);
            StatusTF.setText(status);
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Please input digits only for weight,height and matric number.");
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_calcBillBtnActionPerformed

    private void SaveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveBtnActionPerformed
        // TODO add your handling code here:
        try{
            ind++;
            String matricTF=MatricNoTF.getText();
            if(matricTF.equals(""))
                throw new Exception("Please enter matric number");
            int matric=Integer.parseInt(matricTF);

            String name=NameTF.getText();
            if(name.equals(""))
                throw new Exception ("Please enter name");

            String university=UniCB.getSelectedItem().toString();

            String program="";
            if(DegreeRB.isSelected()==false && MasterRB.isSelected()==false)
                throw new Exception("Please choose the program type");
            if(DegreeRB.isSelected())
                program="degree";
            else
                program="master";
        
            String weightTF= WeightTF.getText();
            if(weightTF.equals(""))
                throw new Exception("Please input weight");
            Double weight=Double.parseDouble(weightTF);

            String heightTF= HeightTF.getText();
            if(heightTF.equals(""))
                throw new Exception("Please input height");
            Double height=Double.parseDouble(heightTF);

            String status=StatusTF.getText();
            if(status.equals(""))
                throw new Exception ("Please press the calculate BMI button before adding information");
            String bmiTF=BMITF.getText();
            Double bmi=Double.parseDouble(bmiTF);
            
            stud=new Student(name, matric, status, university, program, weight, height, bmi);
            operation.saveRecord(ind,stud);
            displayTA.setText("The record has been saved successfully in the array of object");
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Please input digits only for weight,height and matric number");
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        
    }//GEN-LAST:event_SaveBtnActionPerformed

