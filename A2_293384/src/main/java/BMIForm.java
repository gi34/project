    private void SearchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchBtnActionPerformed
        // TODO add your handling code here:
        try{
            String targetTF=MatricNoTF.getText();
            if(targetTF.equals(""))
                throw new Exception("Please enter the matric number.");
            
            int target=Integer.parseInt(targetTF);
            stud=operation.searchRecord(target);

            if(stud==null){
                displayTA.setText("The record is not found.");
            }else{
                NameTF.setText(stud.getName());
                MatricNoTF.setText(String.valueOf(stud.getMatric()));
                WeightTF.setText(String.valueOf( stud.getWeight()));
                HeightTF.setText(String.valueOf(stud.getHeight()));
                BMITF.setText(String.valueOf(stud.getbmi()));
                StatusTF.setText(stud.getStatus());
                UniCB.setSelectedItem(stud.getUniversity());
                if(stud.getProgram().equals("degree"))
                    buttonGroup1.setSelected(DegreeRB.getModel(), true);
                else
                    buttonGroup1.setSelected(MasterRB.getModel(), true);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
   
    }//GEN-LAST:event_SearchBtnActionPerformed

