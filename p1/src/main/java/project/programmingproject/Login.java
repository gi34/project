    private void loginBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginBTNActionPerformed
        // TODO add your handling code here:
        
        try{
            String userType=userTypeCB.getSelectedItem().toString();
            
            String matric =matricTF.getText();
            if(matric.equals(""))
                throw new Exception("Please input matric number");

            String password=passwordTF.getText();
            if(password.equals(""))
                throw new Exception ("Please input password.");
            
            if(userType.equals("Coordinator")){
                userInterface jfrm=new userInterface();
                boolean proceed=co.Login(matric, password);
                if(proceed==true){
                    jfrm.show();
                    dispose();
                }
                else
                    throw new Exception("Invalid log in");
            }
            else if(userType.equals("Supervisor")){
                SupervisorGUI jfrm1=new SupervisorGUI();
                boolean proceed=s.Login(matric, password);
                if(proceed==true){
                    jfrm1.update(matric);
                    jfrm1.show();
                    dispose();
                }
                else 
                    throw new Exception("Invalid log in");
            }
            else if(userType.equals("Evaluator")){
                EvaluatorGUI jfrm2=new EvaluatorGUI();
                boolean proceed=eva.Login(matric, password);
                if(proceed==true){
                    jfrm2.update(matric);
                    jfrm2.show();
                    dispose();
                }
                else
                    throw new Exception("Invalid log in");
            }
            else if(userType.equals("Student")){
                boolean user=stud.Login(matric, password);
                Student ppl=ope.search(matric);
                Interface jfrm3=new Interface(ppl);
                if(user!=false){
                    jfrm3.show();
                    dispose();
                }else
                    throw new Exception("Invalid log in");
            }
            else
                JOptionPane.showMessageDialog(null, "Invalid log in. Wrong password / matric no.");               
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        
        
        
    }//GEN-LAST:event_loginBTNActionPerformed

