    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        // TODO add your handling code here:
        stud=ope.search(matricTF.getText());
        
        if(stud!=null){
            projectTF.setText(stud.getProject());
            proposalMarkTF.setText(String.valueOf(stud.getproposalMark()));
            articleMarkTF.setText(String.valueOf(stud.getarticleMark()));
            presentationMarkTF.setText(String.valueOf(stud.getpresentationMark()));
            finalMarkTF.setText(String.valueOf(stud.getfinalReport()));
            TotalMarkTF.setText(String.valueOf(stud.gettotalMark()));
            GradeTF.setText(String.valueOf(stud.getGrade()));
            pointerTF.setText(String.valueOf(stud.getPointer()));
            EvaluatorCB.setSelectedItem(stud.getEvaluator());
        }
        else
            JOptionPane.showMessageDialog(null, "No record");

    }//GEN-LAST:event_searchBtnActionPerformed

