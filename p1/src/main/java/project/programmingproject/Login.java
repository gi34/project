    private void registerBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerBTNActionPerformed
        // TODO add your handling code here:
        
        try{
            String name=nameTF.getText();
            if(name.equals(""))
            throw new Exception ("Plesase input name");
        
            String matric =matricTF.getText();
            if(matric.equals(""))
                throw new Exception("Please input matric number");

            int semester=Integer.parseInt(semesterTF.getText());
            if(semester<=0||semester >8)
                throw new Exception ("Please input semester(1-8).");

            String password=passwordTF.getText();
            if(password.equals(""))
                throw new Exception ("Please input password.");

            ope.register(name, password, semester, matric);
       //     JOptionPane.showMessageDialog(null, "Registered sucessfully");
   
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Please input matric, name,semester and password");
        }
    }//GEN-LAST:event_registerBTNActionPerformed

