    private void udpateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_udpateBtnActionPerformed
        // TODO add your handling code here:
        String matricNo = matricTF.getText();
        String evaluator=EvaluatorCB.getSelectedItem().toString();
        double total=Double.parseDouble(TotalMarkTF.getText());
        String grade=GradeTF.getText();
        double pointer=Double.parseDouble(pointerTF.getText());
        try{
            if(stud!=null){
                co.setEvaluator(stud, evaluator);
                co.settotal(stud, total);
                co.setGrade(stud, grade);
                co.setPointer(stud, pointer);
                ope.writeToFile();
                JOptionPane.showMessageDialog(null, "Update successful.");
            }
            else {
                JOptionPane.showMessageDialog(null, "Update failed. Please try again");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_udpateBtnActionPerformed

